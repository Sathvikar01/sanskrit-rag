"""HyDE (Hypothetical Document Embeddings) and Cross-Encoder Re-ranking.

HyDE: Generate hypothetical ideal answer, embed it, use for retrieval.
Cross-Encoder Re-ranking: Use BGE-reranker to re-score top candidates.
Query Transformation: Generate sub-queries for comprehensive retrieval.
"""
import re
import time
import json
from pathlib import Path
from typing import List, Dict, Any, Optional, Tuple
from dataclasses import dataclass, field
import numpy as np
import requests

import sys
